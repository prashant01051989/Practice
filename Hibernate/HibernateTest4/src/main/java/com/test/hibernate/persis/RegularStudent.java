package com.test.hibernate.persis;

import java.util.List;

public class RegularStudent extends CurrentStudent{
private String qualification;
private String percentage;
private int yoe;
public RegularStudent() {
	super();
}

public RegularStudent(String sname, String city, String status, float totalFee,
		List<String> emails, List<Long> phones, double feeBal, String timing,
		String branch, String qualification, String percentage, int yoe) {
	super(sname, city, status, totalFee, emails, phones, feeBal, timing, branch);
	this.qualification = qualification;
	this.percentage = percentage;
	this.yoe = yoe;
}
public String getQualification() {
	return qualification;
}
public void setQualification(String qualification) {
	this.qualification = qualification;
}
public String getPercentage() {
	return percentage;
}
public void setPercentage(String percentage) {
	this.percentage = percentage;
}
public int getYoe() {
	return yoe;
}
public void setYoe(int yoe) {
	this.yoe = yoe;
}

}
